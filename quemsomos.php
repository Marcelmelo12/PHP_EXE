	<p>No dia 5 de setembro, 24 equipas partiram em busca do título de campeão de um Major de CS:GO. A Fase dos Desafiadores reduziu esse número de 24 para 16, e a Fase das Lendas determinou as 8 equipas que irão avançar para a Fase dos Campeões em Londres, na Wembley Arena. As equipas Complexity e Liquid terminaram a 3 a 0; os Astralis, Big e Na’Vi a 3 a 1; já os Faze, Hellraisers e mibr conseguiram o resultado de 3 a 2.</p>
